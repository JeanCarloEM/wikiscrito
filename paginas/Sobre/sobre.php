    <div class="cartaz missao">    
      <h6>Missão</h6>
      Ser um canal mundial indenpendente, dinâmico, sério e de credibilidade inquestionável que propicia a informação biblico-teológica sob o maior número de interpretações possíveis de forma justa, imparcial, completa, racional, confiável, organizada e gratuita a todas os interessados, de acadêmicos a leigos, contando para isto com a utilização e construção colaborativa de conteúdo por diversos usuários da internet em âmbito mundial.
    </div>  

    <div class="cartaz visao">    
      <h6>Visão</h6>
      Ser um comentário bíblico de referência mundial acadêmica e popular, destaque nas universidades, igrejas, mídia e redes sociais. Usado cientificamente e popularmente como melhor meio de obter informações bíblicas completas e confiáveis, para diversos fins, inclusive na construção de teses, sermões e artigos científicos.
    </div>  

    <div class="cartaz valores">    
