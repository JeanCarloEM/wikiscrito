  <h1>CONHEÇA O <BIG>WIKISCRITO</BIG></h1>
    
  <div class="limitartamanho">
    <p>O <b>Wikiscrito</b> é um comentário bíblico, e por comentário bíblico entenda-se comentário da bíblia, pois o mesmo não somente trará artigos verso a verso, mas artigos abrangendo todas as questões e teologias levantadas ou envolvidas na bíblia sagrada, levando como premissa a sua inspiração divina, ou seja, o wikiscrito é um comentário teológico, muito embora possa conter muitos elementos e artigos não teológicos, todos que de certa forma contribuiam para fazer do Wikiscrito o mais completo "<a href="//pt.wiktionary.org/wiki/comp%C3%AAndio">compêndio</a>" biblico-teológico, ao ponto de o leitor, leigo ou academico não precisar utiliza-se de outras fontes para realizar seus estudos, uma vez que o Wikiscrito já traria todas as informações necessárias, de forma justa, imparcial, verificavel e confiável.
    </p>

    <p>
      Inspirado em um dos sites que se tornou referência, a <a href="//wikipedia.org">Wikipedia</a>, o Wikiscrito será livre, disponibilizado na forma de <a href="//pt.wikipedia.org/wiki/Wiki">wiki</a>, permitindo que todos, leigos ou doutos contribuam editando, alterando, aprimorando e criando artigos, segundo regras e politicas estritamente criadas pela Organização Wikinalz e deliberada pela comunidade que permitam aos artigos atingir grau de credibilidade condizentes com as diretrizes do Wikiscrito.    
    </p>
  </div>
  
